package Chapter3;

public class ArithmeticOperationsCh3 {

	public static void main(String[] args) {
		// In chapter 3, we learn more about how variables and arithmetic operations work
		int x = 3 + 5 * 3;
		double y = (double)5 / 2;
		String hi = "hello there";
		String message;
		
		message = "As the train left the station, " +
		"the conductor yelled, \"All aboard!\"";
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(hi);
		System.out.println(message);
	}

}
